    public void MostrarTabla() {
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Correo");
        modelo.addColumn("Password");
        //mostramos la tabla con sus encabezados
        TCorreoU.setModel(modelo);
        //se conecta la tabla
        String[] datos = new String[5];

        try {
            Statement st = cn.createStatement();

            ResultSet rs = st.executeQuery("SELECT t2.id, t2.nombre, t1.correo, t1.pass FROM correos AS t1\n"
                    + "INNER JOIN usuarios AS t2 ON t1.id_usuario=t2.id");
            while (rs.next()) {

                datos[0] = rs.getString("t2.id");
                datos[1] = rs.getString("t2.nombre");
                datos[2] = rs.getString("t1.correo");
                datos[3] = rs.getString("t1.pass");

                modelo.addRow(datos);
            }
            TCorreoU.setModel(modelo);

        } catch (SQLException ex) {
            Logger.getLogger(NCorreoU.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }

