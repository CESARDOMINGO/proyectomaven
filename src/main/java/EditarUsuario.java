    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //---------- Encriptar-------------------
        String pas = TPassAjuste.getText();
        textEncryptor.setPassword("123");
        String pass = textEncryptor.encrypt(pas);
        if (JOptionPane.showConfirmDialog(rootPane, "Estas editando un Correo, deseas continuar?",
                "Editar Producto", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            try {
                PreparedStatement pst = cn.prepareStatement("UPDATE usuarios t1, correos t2 SET t1.nombre='"
                        + TNombreAjuste.getText()
                        + "',t2.correo='" + TCorreoAjuste.getText()
                        + "',t2.pass='" + pass
                        + "'WHERE t1.id = '" + tfIdAjuste.getText() + "' AND t1.id= t2.id_usuario");
                pst.executeUpdate();
            } catch (SQLException ex) {

            }
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

